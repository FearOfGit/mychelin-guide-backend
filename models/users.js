const generateToken = (email, nickname) => {
  const { _id } = findUserByEmail(email);

  console.log(process.env.JWT_SECRET_KEY);

  const token = jwt.sign({ _id, email, nickname }, process.env.JWT_SECRET_KEY, {
    expiresIn: '7d',
  });

  return token;
};

