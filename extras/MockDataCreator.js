const createTable = () => {
  const table = {
    KO01: [],
    CH02: [],
    JP03: [],
    WE04: [],
    BS05: [],
    BG06: [],
    CK07: [],
    PZ08: [],
    GB09: [],
    DS10: [],
    VG11: [],
    PB12: [],
  };

  stores.forEach((store) => {
    store.targets.forEach((target) => {
      table[target].push(store);
    });
  });

  const votes = [];

  const categoryCodes = [
    'KO01',
    'CH02',
    'JP03',
    'WE04',
    'BS05',
    'BG06',
    'CK07',
    'PZ08',
    'GB09',
    'DS10',
    'VG11',
    'PB12',
  ];

  const getRandomNumber = (categoryCode) =>
    Math.floor(Math.random() * table[categoryCode].length);

  const curDate = new Date();

  users.forEach((user) => {
    const used = [];
    const curVotes = categoryCodes.map((categoryCode) => {
      const randomIdx = getRandomNumber(categoryCode); //
      const newVote = {
        storeId: table[categoryCode][randomIdx].storeId,
        email: user.email,
        categoryCode,
        voteAt: curDate,
      };
      if (used.includes(newVote.storeId)) return 'x';
      else used.push(newVote.storeId);

      return newVote;
    });
    votes.push(...curVotes);
  });

  return votes;
};

createTable();

